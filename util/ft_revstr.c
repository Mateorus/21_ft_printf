#include "ft_printf.h"

void	ft_revstr(char *s, size_t len)
{
	size_t	i;
	char	tmp;

	i = 0;
	len--;
	while (i < len)
	{
		tmp = s[i];
		s[i] = s[len];
		s[len] = tmp;
		i++;
		len--;
	}
}
